#Estrutura de repeticao

#Enquanto
limite = 0

while limite < 10:
    print(f"Viagem {limite}")
    limite = limite + 1

#Repeticao com for
#Forma simples
for contador in range(20):
        print(f"Viagem com for: {contador}")

#Ler uma lista
caixa_frutas = ["Maçã","Pera","Melancia"]
for fruta in caixa_frutas:
    print(f"A fruta é: {fruta}")